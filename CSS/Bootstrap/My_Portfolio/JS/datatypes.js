//Data types( Primitive types)
//Number - Numeric Values
let age =32;
console.log("The type of age is", typeof age);
console.log("The type of age is"+ age);

//String - Represents a textual data ( enclosed in '' or "")
let name ="Regel";
let address ="#1234, New york City"
console.log("The type of name is :", typeof name);
console.log("The type of address is :", typeof address);

//Boolean - Either true or false
let isAdmin = false;
isAdmin = true;
console.log("The value of  isAdmin is:",typeof isAdmin)

//Null - intentional absense of data
let car =null;
console.log("The value of car",car);
console.log("The type of car",typeof car);

//Undefined - Unintentional absense of data
let city;
console.log("The type of City is:",city);
